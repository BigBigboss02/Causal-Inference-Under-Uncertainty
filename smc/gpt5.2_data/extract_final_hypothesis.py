"""
script to extract the final hypothesis generated by LLM
right before all boxes are opened
used by LLM-PS and LLM-PS-S

see extract_final_hypothesis_partial_obs.py for LLM-PS-P
as logs are of a different format
"""


from __future__ import annotations

import argparse
import json
import pathlib
import re
from typing import Any, Optional


def last_hypothesis_from_history(history: list[dict[str, Any]]) -> Optional[str]:
    for entry in reversed(history):
        h = entry.get("hypothesis")
        if h is not None:
            return h
    return None


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "scan_dir",
        type=pathlib.Path,
        help="Directory containing per-run JSON files",
    )
    parser.add_argument(
        "--file-regex",
        type=str,
        required=True,
        help=r"Regex to match filenames, must capture run number as group(1). Example: ^llm_ps_deterministic_run_(\d+)\.json$",
    )
    parser.add_argument(
        "-o",
        "--output",
        type=pathlib.Path,
        default=None,
        help="Output JSON path (default: <scan_dir>/llm_ps_*_final_hypotheses.json)",
    )
    parser.add_argument(
        "--text-output",
        type=pathlib.Path,
        default=None,
        help="Plain-text path: last hypotheses only, separated by blank lines (default: same basename as JSON with .txt)",
    )
    args = parser.parse_args()

    scan_dir: pathlib.Path = args.scan_dir.resolve()
    file_re = re.compile(args.file_regex)

    matches: list[tuple[int, pathlib.Path]] = []
    for p in scan_dir.iterdir():
        if not p.is_file():
            continue
        m = file_re.match(p.name)
        if m:
            matches.append((int(m.group(1)), p))
    matches.sort(key=lambda t: t[0])

    if args.output is None:
        out_path = scan_dir / "llm_ps_final_hypotheses.json"
    else:
        out_path = args.output.resolve()

    text_path = args.text_output
    if text_path is None:
        text_path = out_path.with_suffix(".txt")
    text_path = text_path.resolve()

    rows: list[dict[str, Any]] = []
    for file_index, p in matches:
        with p.open(encoding="utf-8") as f:
            data = json.load(f)
        meta = data.get("meta") or {}
        run_number = meta.get("run_number", file_index)
        history = data.get("history") or []
        rows.append(
            {
                "source_file": p.name,
                "file_index": file_index,
                "run_number": run_number,
                "solved": data.get("solved"),
                "trials": data.get("trials"),
                "opened": data.get("opened"),
                "last_hypothesis": last_hypothesis_from_history(history),
            }
        )

    rows.sort(key=lambda r: (int(r["run_number"]), r["file_index"]))
    payload = {"scan_dir": str(scan_dir), "count": len(rows), "runs": rows}
    with out_path.open("w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2)
    print(f"Wrote {out_path} ({len(rows)} run files)")

    hyps = [r["last_hypothesis"] for r in rows if r.get("last_hypothesis")]
    with text_path.open("w", encoding="utf-8") as f:
        f.write("\n\n".join(hyps))
    print(f"Wrote {text_path} ({len(hyps)} hypotheses, text only)")


if __name__ == "__main__":
    main()

